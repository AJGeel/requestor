  <div class="abstract">
    <h2>Abstract</h2>
    <p>This project is guided by the question: <i>"How may we create a better collaborative space for UX Designers and their environment?"</i>. Requestor is an online platform that makes sharing UX works-in-progress fast and easy. The platform allows you to import interactive prototypes from popular prototyping tools and append an interactive request for feedback within minutes. <br/><br/> In this document, we explain the academic evaluation that was conducted to evaluate the platform's qualities and viability for design.</p>
